    ////a backtracking algorithm. to create Solution
    private boolean sudoku(String [][] solvedBoard){
        for (int i = 0; i < 9; i++){
            for (int j = 0; j < 9; j++){
                if (solvedBoard[i][j].equals(Character. toString('.'))){// check for empty box in puzzle
                    
                    for (char num = '1'; num <= '9'; num++){//try each num
                        if (isValid(solvedBoard, i, j, num)){//if num is correct
                            solvedBoard[i][j] = Character. toString(num);// fill the correct num in empty bix
                            if (sudoku(solvedBoard)){// recurrsin call to further empty boxes
                                return true;
                            }else{ // if further empty boxs have no valid num then return to the last reccursion and try with another num
                                solvedBoard[i][j] = ".";
                            }
                        }
                    }
                    return false;// if in reccursin call no valid num is found
                }
            }
        }
        return true;
        
    }
    //three rules of game 
    /*1. All the rows should be filled with numbers(1 – 9) exactly once.
    2. All the columns should be filled with numbers(1 – 9) exactly once.
    3. Each 3×3 submatrix should be filled with numbers(1 – 9) exactly once.*/
    private boolean isValid(String [][] solvedBoard, int row, int col, char num){
        for (int i = 0; i<9; i++){
            if (solvedBoard[row][i].equals(Character. toString(num)))
                return false; // 1. if num is present in perticular rowwise
            if (solvedBoard[i][col].equals(Character.toString(num)))
                return false; // 2. if num is present in perticular colwise
            if (solvedBoard[3 * (row/3) + i/3][3 * (col/3) + i%3].equals
                (Character.toString(num))) 
            return false; // 3. if num is present in 3x3 in perticular submatrix
        }
        return true;
    }
    
    //// UnDo functionality
    private void undo(){
        if (!st.empty()){//if empty do nothing to tackel error.
            HashMap<JButton,String> delete = st.pop();
            HashMap.Entry<JButton,String> entry = delete.entrySet().stream().findFirst().get();
            JButton key = entry.getKey();// get the peek (only key) stored in stack
            
            boolean flag = false;
            
            Stack<HashMap<JButton,String>> temp = new Stack<>();
            
            while(!st.empty()){// iterate through remaining stack
                // check further ele in stack
                HashMap<JButton,String> t = st.pop();//
                temp.push(t);// push it to temp stack
                        
                HashMap.Entry<JButton,String> entry1 = t.entrySet().stream().findFirst().get();
                JButton key1 = entry1.getKey();
                String value = entry1.getValue();// if occurrence is found then put this value on btn
                
                if (key == key1){// if first occurrence of same btn found
                    key.setText(value);
                    flag = true;
                    break;// break to only get fisrt occurrence
                }
            }
            if (flag == false){// if btn has no further occurrence
                key.setText("");// set it to empty
            }
            while(!temp.empty()){// again push to original stack
                HashMap<JButton,String> t = temp.pop();
                st.push(t);
            }
        }
    }
    
